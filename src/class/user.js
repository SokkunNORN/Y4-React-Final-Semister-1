export class User {
    constructor (id, age, email, fullName, gender, phone, province, username, createdAt, updatedAt) {
        this.id = id;
        this.age = age;
        this.email = email;
        this.fullName = fullName;
        this.gender = gender;
        this.phone = phone;
        this.province = province;
        this.username = username;
        this.createdAt = createdAt;
        this.updatedAt = updatedAt;
    }
}